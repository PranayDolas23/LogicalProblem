package GetterSetter;

public class Hostel {
	
	private long roomno;
	private String studentnm;
	private float studentweight;
	private int year;
	public long getRoomno() {
		return roomno;
	}
	public void setRoomno(long roomno) {
		this.roomno = roomno;
	}
	public String getStudentnm() {
		return studentnm;
	}
	public void setStudentnm(String studentnm) {
		this.studentnm = studentnm;
	}
	public float getStudentweight() {
		return studentweight;
	}
	public void setStudentweight(float studentweight) {
		this.studentweight = studentweight;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}

}
