package HasAgetterSetter;

public class Salary {
	
	int originalSalary;
	int extraSalary;
	
	public void setOriginalSalary(int originalSalary)
	{
		this.originalSalary=originalSalary;
	}
	public int getOriginalSalary()
	{
		return originalSalary;
	}
	
	public void setExtraSalary(int extrasalary)
	{
		this.extraSalary=extrasalary;
	}
	public int getExtraSalary()
	{
		return extraSalary;
	}

}
